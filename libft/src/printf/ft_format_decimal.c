#include "ft_printf.h"

int	ft_format_decimal(int i)
{
	int	n;

	ft_putnbr_fd(i, 1);
	n = 0;
	if (i == 0)
		return (1);
	if (i < 0)
		n++;
	while (i != 0)
	{
		i /= 10;
		n++;
	}
	return (n);
}
